//README

//OBJECTIVE
//The objective of this project is to create a memory blackjack game where user plays 
//races against an AI to be the first to sore 21.

//RULE
//Game will continue until user or opponent wins the game by either:
//1.Being the first to hit 21.
//2.Not busing by going past 21.

//OPERATION
//Player clicks left mouce button to pick cards.
//Player clicks Change Image button to change the background image of cards.
//In Editor mode, press key A to reveal the card value on the table.
//In Editor mode, press key S to cover the card value on the table.
//In Editor mode, Press key D to let CPU/Opponent pick two cards with same value.